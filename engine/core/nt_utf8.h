// clang-format on

static inline uint32_t nt_utf8_decode(uint32_t *state, uint32_t *codep, uint32_t byte) {
    uint32_t type = nt_utf8d[byte];
    *codep = (*state != NT_UTF8_ACCEPT) ? (byte & 0x3FU) | (*codep << 6) : (0xFFU >> type) & byte;
    *state = nt_utf8d[256 + *state + type];
    return *state;
}

